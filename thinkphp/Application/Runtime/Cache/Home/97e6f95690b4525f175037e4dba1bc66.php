    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;汉中面皮起源于秦汉时期，相传刘邦在汉中称王，命萧何修筑山河堰，粮食连年丰收。农民为改善生活，把面粉加水稀释，蒸成薄饼，切条凉拌而食，味道很美。一日，刘邦微服出访，进入百姓之家，好客的农民便以凉拌蒸饼丝条招待。刘邦边吃边称赞，问及名称，农民说不出来，刘邦听了制作方法哈哈一笑，信口说道：“此乃蒸饼也。”后来，人们改用重叠式竹笼，一次可蒸数张，而且又大又簿。切成细条，筋丝柔韧，软而不断，恰像皮条，改名“面皮”。 </p>
     
